/* This code snippet is a `switch` statement in JavaScript. It is used to evaluate the `key` variable
and execute different blocks of code based on its value. */
// switch (key) {
//     case value:
        
//         break;

//     default:
//         break;
// }


const month=3

switch (month) {
    case 1:                       // if string use " " eg."jan"
        console.log("January")
        break;
    case 2:
        console.log("February")
        break;
    case 3:
        console.log("March")
        break;
    case 4:
        console.log("April")
        break;

    default:
        console.log("Default case match")
        break;
}



